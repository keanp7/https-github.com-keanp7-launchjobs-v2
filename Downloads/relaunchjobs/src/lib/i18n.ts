export function t(lang: Lang, path: string, vars?: Record<string, string | number>): string {
  const keys = path.split(".")
  let val: any = translations[lang]
  for (const k of keys) {
    val = val?.[k]
    if (val === undefined) {
      // fallback to english
      let fb: any = translations.en
      for (const fk of keys) fb = fb?.[fk]
      val = fb ?? path
      break
    }
  }
  if (typeof val !== "string") return path
  if (vars) {
    return val.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? `{${k}}`))
  }
  return val
}
